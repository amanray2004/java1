package JAVA.staticexamples;

public class Human {
    int age;
    String name;
    int salary;
    boolean married;
    public Human(int age, String name, int salary,boolean married) {
        this.age = age;
        this.name = name;
        this.salary = salary;
        this.married=married;
    }
    public Human() {
        this.age = 0;
        this.name = null;
        this.salary = 0;
        
    }
    
    
}
